<script>
    $(document).ready(function(){
        applyMask('+7'); // Начальная маска для России

        $('#countryCode').change(function() {
            applyMask($(this).val());
        });

        function applyMask(countryCode) {
            if (countryCode === '+7') {
                $('#phone_number').mask('+7 (000) 000-00-00');
            } else if (countryCode === '+375') {
                $('#phone_number').mask('+375 (00) 000-00-00');
            }
        }
    });

</script>
